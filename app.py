def on_forecast_run(df, value_col, periods):
    if df is None or df.empty or not value_col:
        return _empty_fig_placeholder(), "Select a numeric column and ensure your dataset has a date column."

    working_df = df
    date_col = detect_date_column(working_df)

    if date_col is None:
        # Auto-attempt date conversion so forecasting works even if the user
        # hasn't manually run the cleaning pipeline first.
        working_df = df.copy()
        for col in working_df.columns:
            if pd.api.types.is_object_dtype(working_df[col]) or pd.api.types.is_string_dtype(working_df[col]):
                sample = working_df[col].dropna().astype(str).head(20)
                if sample.empty:
                    continue
                try:
                    parsed = pd.to_datetime(sample, errors="coerce", format="mixed")
                    if parsed.notna().mean() > 0.8:
                        working_df[col] = pd.to_datetime(working_df[col], errors="coerce", format="mixed")
                except Exception:
                    continue
        date_col = detect_date_column(working_df)

    if date_col is None:
        return _empty_fig_placeholder(), "No date/datetime column detected in this dataset. Forecasting needs at least one date column."

    if value_col not in numeric_columns(working_df):
        return _empty_fig_placeholder(), f"'{value_col}' is not numeric. Please select a numeric column to forecast."

    try:
        series = prepare_series(working_df, date_col, value_col, freq="ME")
        if series.dropna().empty:
            return _empty_fig_placeholder(), "Not enough data points to forecast."

        forecast_vals, method = forecast_series(series, periods=int(periods))
        fig = build_forecast_chart(series, forecast_vals, value_col, method)
        growth = compute_growth_rate(series)
        seasonality = detect_seasonality(series) or "Not enough data to assess seasonality."

        summary = (
            f"Detected date column: **{date_col}**\n"
            f"Overall growth rate: **{growth:.1f}%**\n"
            f"{seasonality}\n"
            f"Forecast method: **{method}**"
        )
        return fig, summary
    except Exception as e:
        return _empty_fig_placeholder(), f"Forecasting failed: {e}"


