def numeric_columns(df: pd.DataFrame) -> List[str]:
    return df.select_dtypes(include=np.number).columns.tolist()


def categorical_columns(df: pd.DataFrame) -> List[str]:
    return df.select_dtypes(include=["object", "string", "category", "bool"]).columns.tolist()


def datetime_columns(df: pd.DataFrame) -> List[str]:
    return df.select_dtypes(include=["datetime64[ns]", "datetime64"]).columns.tolist()


def correlation_matrix(df: pd.DataFrame) -> pd.DataFrame:
    num_cols = numeric_columns(df)
    if len(num_cols) < 2:
        return pd.DataFrame()
    return df[num_cols].corr(numeric_only=True).round(3)


def top_correlations(df: pd.DataFrame, n: int = 5) -> Dict[str, list]:
    corr = correlation_matrix(df)
    if corr.empty:
        return {"highest": [], "lowest": []}

    pairs = []
    cols = corr.columns
    for i in range(len(cols)):
        for j in range(i + 1, len(cols)):
            val = corr.iloc[i, j]
            if pd.notna(val):
                pairs.append((cols[i], cols[j], val))

    pairs_sorted_desc = sorted(pairs, key=lambda x: abs(x[2]), reverse=True)
    pairs_sorted_asc = sorted(pairs, key=lambda x: abs(x[2]))

    return {
        "highest": pairs_sorted_desc[:n],
        "lowest": pairs_sorted_asc[:n],
    }


def detect_outliers_zscore(df: pd.DataFrame, threshold: float = OUTLIER_Z_THRESHOLD) -> Dict[str, int]:
    result = {}
    for col in numeric_columns(df):
        series = df[col].dropna()
        if len(series) < 3 or series.std() == 0:
            result[col] = 0
            continue
        z_scores = np.abs(stats.zscore(series))
        result[col] = int((z_scores > threshold).sum())
    return result


def detect_outliers_iqr(df: pd.DataFrame) -> Dict[str, int]:
    result = {}
    for col in numeric_columns(df):
        series = df[col].dropna()
        if len(series) < 4:
            result[col] = 0
            continue
        q1, q3 = series.quantile(0.25), series.quantile(0.75)
        iqr = q3 - q1
        lower, upper = q1 - 1.5 * iqr, q3 + 1.5 * iqr
        result[col] = int(((series < lower) | (series > upper)).sum())
    return result


def skewness_kurtosis(df: pd.DataFrame) -> pd.DataFrame:
    rows = []
    for col in numeric_columns(df):
        series = df[col].dropna()
        if len(series) < 3:
            continue
        rows.append({
            "Column": col,
            "Skewness": round(float(stats.skew(series)), 3),
            "Kurtosis": round(float(stats.kurtosis(series)), 3),
        })
    return pd.DataFrame(rows)


def normality_check(df: pd.DataFrame) -> pd.DataFrame:
    rows = []
    for col in numeric_columns(df):
        series = df[col].dropna()
        if len(series) < 8:
            continue
        sample = series.sample(min(len(series), 4999), random_state=42)
        try:
            stat_val, p_value = stats.shapiro(sample)
            rows.append({
                "Column": col,
                "Statistic": round(float(stat_val), 4),
                "P-Value": round(float(p_value), 4),
                "Normally Distributed": "Yes" if p_value > 0.05 else "No",
            })
        except Exception:
            continue
    return pd.DataFrame(rows)


def variance_analysis(df: pd.DataFrame) -> pd.DataFrame:
    rows = []
    for col in numeric_columns(df):
        series = df[col].dropna()
        if series.empty:
            continue
        rows.append({
            "Column": col,
            "Variance": round(float(series.var()), 3),
            "Std Dev": round(float(series.std()), 3),
            "Mean": round(float(series.mean()), 3),
            "CV (%)": round(float(series.std() / series.mean() * 100), 2) if series.mean() != 0 else np.nan,
        })
    return pd.DataFrame(rows)


def descriptive_summary(df: pd.DataFrame) -> pd.DataFrame:
    num_cols = numeric_columns(df)
    if not num_cols:
        return pd.DataFrame()
    desc = df[num_cols].describe(percentiles=[0.05, 0.25, 0.5, 0.75, 0.95]).T.round(3)
    return desc


