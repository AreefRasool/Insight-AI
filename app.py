def dataset_health_score(df: pd.DataFrame) -> Dict[str, float]:
    total_cells = df.shape[0] * df.shape[1] if df.size else 1
    missing_ratio = df.isna().sum().sum() / total_cells
    duplicate_ratio = df.duplicated().sum() / max(len(df), 1)

    completeness_score = (1 - missing_ratio) * 100
    uniqueness_score = (1 - duplicate_ratio) * 100
    consistency_score = 100.0

    overall = round(0.5 * completeness_score + 0.3 * uniqueness_score + 0.2 * consistency_score, 1)
    return {
        "overall": min(max(overall, 0), 100),
        "completeness": round(completeness_score, 1),
        "uniqueness": round(uniqueness_score, 1),
        "consistency": round(consistency_score, 1),
    }


def generate_business_insights(df: pd.DataFrame) -> List[str]:
    insights: List[str] = []
    num_cols = numeric_columns(df)
    cat_cols = categorical_columns(df)
    date_cols = datetime_columns(df)

    insights += _missing_value_insights(df)
    insights += _correlation_insights(df, num_cols)
    insights += _distribution_insights(df, num_cols)
    insights += _categorical_insights(df, cat_cols)
    insights += _trend_insights(df, date_cols, num_cols)

    if not insights:
        insights.append("Dataset looks clean and well-balanced - no major issues detected.")

    return insights


def _missing_value_insights(df: pd.DataFrame) -> List[str]:
    out = []
    total = len(df)
    if total == 0:
        return out
    missing_pct = (df.isna().sum() / total).sort_values(ascending=False)
    flagged = missing_pct[missing_pct > MISSING_WARNING_THRESHOLD]
    for col, pct in flagged.items():
        out.append(f"Column '{col}' has {pct*100:.1f}% missing values - consider imputation or removal.")
    dup_pct = df.duplicated().sum() / total
    if dup_pct > 0.01:
        out.append(f"{dup_pct*100:.1f}% of rows are exact duplicates - recommend de-duplication.")
    return out


def _correlation_insights(df: pd.DataFrame, num_cols: List[str]) -> List[str]:
    out = []
    if len(num_cols) < 2:
        return out
    corr_info = top_correlations(df, n=3)
    for a, b, val in corr_info.get("highest", []):
        if abs(val) >= HIGH_CORRELATION_THRESHOLD:
            direction = "positively" if val > 0 else "negatively"
            out.append(f"'{a}' and '{b}' are strongly {direction} correlated (r = {val:.2f}).")
    return out


def _distribution_insights(df: pd.DataFrame, num_cols: List[str]) -> List[str]:
    out = []
    outliers = detect_outliers_iqr(df)
    for col, count in outliers.items():
        if count > 0:
            pct = count / max(len(df), 1) * 100
            if pct > 1:
                out.append(f"'{col}' contains {count} outlier(s) ({pct:.1f}% of data) using the IQR method.")

    for col in num_cols:
        series = df[col].dropna()
        if series.empty:
            continue
        mean_val, max_val, min_val = series.mean(), series.max(), series.min()
        out.append(f"'{col}' averages {mean_val:,.2f} (range: {min_val:,.2f} -> {max_val:,.2f}).")
    return out[: min(len(out), 8)]


def _categorical_insights(df: pd.DataFrame, cat_cols: List[str]) -> List[str]:
    out = []
    total = len(df)
    if total == 0:
        return out
    for col in cat_cols:
        series = df[col].dropna()
        if series.empty:
            continue
        value_counts = series.value_counts(normalize=True)
        top_value, top_share = value_counts.index[0], value_counts.iloc[0]

        if top_share >= IMBALANCE_THRESHOLD:
            out.append(f"Column '{col}' is highly imbalanced - '{top_value}' makes up {top_share*100:.1f}% of all records.")
        else:
            out.append(f"The most frequent value in '{col}' is '{top_value}' ({top_share*100:.1f}% share).")

        rare = value_counts[value_counts < RARE_CATEGORY_THRESHOLD]
        if len(rare) > 0:
            out.append(f"'{col}' has {len(rare)} rare categor{'y' if len(rare)==1 else 'ies'} (each under {RARE_CATEGORY_THRESHOLD*100:.0f}% share).")
    return out[: min(len(out), 8)]


def _trend_insights(df: pd.DataFrame, date_cols: List[str], num_cols: List[str]) -> List[str]:
    out = []
    if not date_cols or not num_cols:
        return out

    date_col = date_cols[0]
    temp = df[[date_col] + num_cols].dropna(subset=[date_col]).sort_values(date_col)
    if temp.empty or len(temp) < 4:
        return out

    for metric in num_cols[:3]:
        series = temp[metric].dropna()
        if len(series) < 4:
            continue
        first_half = series.iloc[: len(series) // 2].mean()
        second_half = series.iloc[len(series) // 2:].mean()
        if first_half == 0 or pd.isna(first_half):
            continue
        growth = (second_half - first_half) / abs(first_half) * 100
        direction = "increased" if growth > 0 else "decreased"
        out.append(f"'{metric}' has {direction} by {abs(growth):.1f}% over the observed time period (based on '{date_col}').")
    return out


def generate_recommendations(df: pd.DataFrame) -> List[str]:
    recs: List[str] = []
    total = len(df)
    if total == 0:
        return ["Upload a non-empty dataset to receive recommendations."]

    missing_pct = df.isna().sum().sum() / (df.shape[0] * df.shape[1])
    if missing_pct > MISSING_WARNING_THRESHOLD:
        recs.append("Run the 'Handle Missing Values' cleaning tool before deeper analysis.")

    if df.duplicated().sum() > 0:
        recs.append("Remove duplicate rows to avoid skewed aggregate statistics.")

    num_cols = numeric_columns(df)
    outliers = detect_outliers_iqr(df)
    if any(v > 0 for v in outliers.values()):
        recs.append("Investigate flagged outliers - they may be data-entry errors or genuine extreme events.")

    cat_cols = categorical_columns(df)
    for col in cat_cols:
        series = df[col].dropna()
        if series.empty:
            continue
        if series.value_counts(normalize=True).iloc[0] >= IMBALANCE_THRESHOLD:
            recs.append(f"'{col}' is imbalanced - consider stratified sampling for any ML model using it.")
            break

    if len(num_cols) >= 2:
        recs.append("Explore the Correlation Matrix tab to identify redundant or highly related features.")

    if not recs:
        recs.append("Dataset is in good shape - proceed directly to visualization and modeling.")

    return recs


