def detect_seasonality(series: pd.Series, period: int = 12) -> Optional[str]:
    series = series.dropna()
    if len(series) < period * 2:
        return None
    try:
        autocorr = series.autocorr(lag=period)
        if pd.isna(autocorr):
            return None
        if autocorr > 0.5:
            return f"Likely seasonal pattern detected (lag-{period} autocorrelation = {autocorr:.2f})."
        return f"No strong seasonality detected (lag-{period} autocorrelation = {autocorr:.2f})."
    except Exception:
        return None


