def load_csv(file_obj) -> Tuple[Optional[pd.DataFrame], str]:
    if file_obj is None:
        return None, "Please upload a CSV file to begin."

    path = file_obj if isinstance(file_obj, str) else file_obj.name
    if not str(path).lower().endswith(SUPPORTED_EXTENSIONS):
        return None, "Unsupported file type. Please upload a .csv file."

    try:
        df = pd.read_csv(path)
    except pd.errors.EmptyDataError:
        return None, "The uploaded file is empty."
    except UnicodeDecodeError:
        try:
            df = pd.read_csv(path, encoding="latin1")
        except Exception as e:
            return None, f"Could not decode file: {e}"
    except Exception as e:
        return None, f"Error reading CSV: {e}"

    if df.empty:
        return None, "The dataset contains no rows."
    if df.shape[1] == 0:
        return None, "The dataset contains no columns."

    return df, f"Successfully loaded **{path.split('/')[-1]}** - {df.shape[0]:,} rows x {df.shape[1]} columns."


def _pick_default_regression_target(df: pd.DataFrame) -> Optional[str]:
    """Pick a sensible default numeric target - avoids empty or mostly-missing
    numeric columns (e.g. a blank 'notes' column) that would otherwise make
    every downstream ML call fail immediately."""
    usable = _usable_numeric_columns(df)
    if usable:
        return usable[-1]
    return df.columns.tolist()[0] if len(df.columns) else None


def _pick_default_classification_target(df: pd.DataFrame) -> Optional[str]:
    """Pick a sensible default target for tree/forest tools - prefers a
    categorical column with reasonable cardinality, avoiding ID/date-like
    columns that have near-unique values per row."""
    cat_cols = categorical_columns(df)
    for col in cat_cols:
        if 1 < df[col].nunique() <= 20:
            return col
    num_cols = numeric_columns(df)
    for col in num_cols:
        if 1 < df[col].nunique() <= 10:
            return col
    return df.columns.tolist()[0] if len(df.columns) else None


