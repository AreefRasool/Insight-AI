def _make_nav_switch_fn(target_page: str):
    """Returns a callback that shows `target_page` and hides all others,
    while also updating sidebar button styling to highlight the active item."""
    def _switch(*_args):
        visibility_updates = [gr.update(visible=(p == target_page)) for p in PAGE_NAMES]
        button_updates = [
            gr.update(elem_classes=["nav-btn-active"] if p == target_page else [])
            for p in PAGE_NAMES
        ]
        return (*visibility_updates, *button_updates)
    return _switch


