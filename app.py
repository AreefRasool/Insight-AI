class DataCleaner:
    def __init__(self, df: pd.DataFrame) -> None:
        self.original_shape = df.shape
        self.df = df.copy()
        self.log: List[str] = []

    def remove_duplicates(self) -> "DataCleaner":
        before = len(self.df)
        self.df = self.df.drop_duplicates()
        removed = before - len(self.df)
        if removed:
            self.log.append(f"Removed {removed:,} duplicate row(s).")
        else:
            self.log.append("No duplicate rows found.")
        return self

    def handle_missing_values(self, strategy: str = "smart") -> "DataCleaner":
        total_missing_before = int(self.df.isna().sum().sum())
        if total_missing_before == 0:
            self.log.append("No missing values detected.")
            return self

        if strategy == "drop":
            before = len(self.df)
            self.df = self.df.dropna()
            self.log.append(f"Dropped {before - len(self.df):,} row(s) containing missing values.")
            return self

        for col in self.df.columns:
            if self.df[col].isna().sum() == 0:
                continue
            if pd.api.types.is_numeric_dtype(self.df[col]):
                fill_value = 0 if strategy == "zero" else self.df[col].median()
                self.df[col] = self.df[col].fillna(fill_value)
            else:
                if strategy == "zero":
                    fill_value = "Unknown"
                else:
                    mode_series = self.df[col].mode()
                    fill_value = mode_series.iloc[0] if not mode_series.empty else "Unknown"
                self.df[col] = self.df[col].fillna(fill_value)

        self.log.append(f"Filled {total_missing_before:,} missing value(s) using '{strategy}' strategy.")
        return self

    def convert_date_columns(self) -> "DataCleaner":
        converted = []
        for col in self.df.columns:
            if pd.api.types.is_object_dtype(self.df[col]) or pd.api.types.is_string_dtype(self.df[col]):
                sample = self.df[col].dropna().astype(str).head(20)
                if sample.empty:
                    continue
                try:
                    parsed = pd.to_datetime(sample, errors="coerce", format="mixed")
                    success_ratio = parsed.notna().mean()
                    if success_ratio > 0.8:
                        self.df[col] = pd.to_datetime(self.df[col], errors="coerce", format="mixed")
                        converted.append(col)
                except Exception:
                    continue
        if converted:
            self.log.append(f"Converted to datetime: {', '.join(converted)}.")
        else:
            self.log.append("No date-like columns detected for conversion.")
        return self

    def remove_empty_columns(self) -> "DataCleaner":
        empty_cols = [c for c in self.df.columns if self.df[c].isna().all()]
        if empty_cols:
            self.df = self.df.drop(columns=empty_cols)
            self.log.append(f"Removed fully empty column(s): {', '.join(empty_cols)}.")
        else:
            self.log.append("No fully empty columns found.")
        return self

    def trim_spaces(self) -> "DataCleaner":
        object_cols = self.df.select_dtypes(include=["object", "string"]).columns
        changed = 0
        for col in object_cols:
            stripped = self.df[col].astype(str).str.strip()
            if not stripped.equals(self.df[col].astype(str)):
                changed += 1
            self.df[col] = stripped.replace({"nan": np.nan, "None": np.nan})
        if changed:
            self.log.append(f"Trimmed leading/trailing spaces in {changed} column(s).")
        else:
            self.log.append("No extra whitespace found in text columns.")
        return self

    def normalize_text(self) -> "DataCleaner":
        object_cols = self.df.select_dtypes(include=["object", "string"]).columns
        for col in object_cols:
            self.df[col] = self.df[col].astype(str).str.replace(r"\s+", " ", regex=True)
        self.log.append(f"Normalized whitespace/casing artifacts in {len(object_cols)} text column(s).")
        return self

    def run_full_pipeline(self) -> Tuple[pd.DataFrame, List[str]]:
        (
            self.trim_spaces()
            .remove_empty_columns()
            .remove_duplicates()
            .convert_date_columns()
            .handle_missing_values(strategy="smart")
        )
        return self.df, self.get_report()

    def get_report(self) -> List[str]:
        after_shape = self.df.shape
        summary = (
            f"Shape changed from {self.original_shape[0]:,} rows x "
            f"{self.original_shape[1]} cols -> {after_shape[0]:,} rows x {after_shape[1]} cols."
        )
        return [summary] + self.log


