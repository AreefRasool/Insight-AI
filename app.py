def _style(fig: go.Figure, title: str) -> go.Figure:
    fig.update_layout(
        template=PLOTLY_TEMPLATE,
        title=dict(text=title, font=dict(size=16, color=COLOR_TEXT)),
        paper_bgcolor=COLOR_BG,
        plot_bgcolor=COLOR_CARD,
        font=dict(color=COLOR_TEXT, family="Inter, sans-serif"),
        margin=dict(l=40, r=30, t=60, b=40),
        legend=dict(bgcolor="rgba(0,0,0,0)"),
        colorway=CHART_COLOR_SEQUENCE,
    )
    return fig


def _sample_if_large(df: pd.DataFrame) -> pd.DataFrame:
    if len(df) > MAX_ROWS_FULL_RENDER:
        return df.sample(MAX_ROWS_FULL_RENDER, random_state=42)
    return df


def bar_chart(df: pd.DataFrame, cat_col: str, num_col: Optional[str] = None, top_n: int = 12) -> Optional[go.Figure]:
    if cat_col not in df.columns:
        return None
    data = df.copy()
    if num_col and num_col in data.columns:
        grouped = data.groupby(cat_col)[num_col].sum().sort_values(ascending=False).head(top_n)
        fig = px.bar(grouped, x=grouped.index, y=grouped.values, labels={"x": cat_col, "y": num_col})
        title = f"{num_col} by {cat_col} (Top {top_n})"
    else:
        counts = data[cat_col].value_counts().head(top_n)
        fig = px.bar(counts, x=counts.index, y=counts.values, labels={"x": cat_col, "y": "Count"})
        title = f"Top Categories - {cat_col}"
    return _style(fig, title)


def line_chart(df: pd.DataFrame, date_col: str, num_col: str) -> Optional[go.Figure]:
    if date_col not in df.columns or num_col not in df.columns:
        return None
    data = df[[date_col, num_col]].dropna().sort_values(date_col)
    if data.empty:
        return None
    fig = px.line(data, x=date_col, y=num_col, markers=True)
    return _style(fig, f"{num_col} Over Time")


def pie_chart(df: pd.DataFrame, cat_col: str, top_n: int = 8) -> Optional[go.Figure]:
    if cat_col not in df.columns:
        return None
    counts = df[cat_col].value_counts().head(top_n)
    fig = px.pie(values=counts.values, names=counts.index, hole=0.45)
    return _style(fig, f"Share Distribution - {cat_col}")


def scatter_plot(df: pd.DataFrame, x_col: str, y_col: str, color_col: Optional[str] = None) -> Optional[go.Figure]:
    if x_col not in df.columns or y_col not in df.columns:
        return None
    data = _sample_if_large(df)
    fig = px.scatter(
        data, x=x_col, y=y_col,
        color=color_col if color_col in data.columns else None,
        trendline="ols" if len(data) > 5 else None,
        opacity=0.75,
    )
    return _style(fig, f"{y_col} vs {x_col}")


def histogram(df: pd.DataFrame, num_col: str, bins: int = 30) -> Optional[go.Figure]:
    if num_col not in df.columns:
        return None
    fig = px.histogram(df, x=num_col, nbins=bins)
    return _style(fig, f"Distribution of {num_col}")


def box_plot(df: pd.DataFrame, num_col: str, cat_col: Optional[str] = None) -> Optional[go.Figure]:
    if num_col not in df.columns:
        return None
    fig = px.box(df, y=num_col, x=cat_col if cat_col in df.columns else None, points="outliers")
    return _style(fig, f"Box Plot - {num_col}")


def violin_plot(df: pd.DataFrame, num_col: str, cat_col: Optional[str] = None) -> Optional[go.Figure]:
    if num_col not in df.columns:
        return None
    fig = px.violin(df, y=num_col, x=cat_col if cat_col in df.columns else None, box=True, points=False)
