def prepare_series(df: pd.DataFrame, date_col: str, value_col: str, freq: str = "ME") -> pd.Series:
    data = df[[date_col, value_col]].dropna().sort_values(date_col)
    data = data.set_index(date_col)
    series = data[value_col].resample(freq).sum()
    return series


def moving_average(series: pd.Series, window: int = 3) -> pd.Series:
    return series.rolling(window=window, min_periods=1).mean()


def compute_growth_rate(series: pd.Series) -> float:
    series = series.dropna()
    if len(series) < 2 or series.iloc[0] == 0:
        return 0.0
    return float((series.iloc[-1] - series.iloc[0]) / abs(series.iloc[0]) * 100)


def forecast_series(series: pd.Series, periods: int = 6) -> Tuple[pd.Series, str]:
    series = series.dropna()
    if len(series) < 4:
        return pd.Series(dtype=float), "insufficient_data"

    try:
        from statsmodels.tsa.holtwinters import Holt
        model = Holt(series, initialization_method="estimated").fit()
        forecast_vals = model.forecast(periods)
        return forecast_vals, "Holt Linear Trend (statsmodels)"
    except Exception:
        x = np.arange(len(series))
        coeffs = np.polyfit(x, series.values, deg=1)
        future_x = np.arange(len(series), len(series) + periods)
        forecast_vals = np.polyval(coeffs, future_x)
        freq = pd.infer_freq(series.index) or "M"
        future_index = pd.date_range(start=series.index[-1], periods=periods + 1, freq=freq)[1:]
        return pd.Series(forecast_vals, index=future_index), "Linear Regression (fallback)"


